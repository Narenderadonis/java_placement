
public class switchcase {

	public static void main(String[] args) {
		int dayofweek=3;
		switch(dayofweek) {
		case 1:
			System.out.println("I am on leave");
			break;
		case 2:
			System.out.println("I am playing cricket");
			break;
		case 3:
			System.out.println("I am styding java");
			break;
		default:
			System.out.println("This is default case");
		}

	}

}
